# Operadores Relacionais > < ==
num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))
print("Primeiro número é maior que segundo número: ", num1 > num2)
print("Primeiro número é menor que segundo número: ", num1 < num2)
print("Primeiro número é igual que o segundo número: ", num1 == num2)

### Format: f para colcoar variaveis dentro da string inteira sem vírgulas, etc
quantidade_real = float(input("Quantos reais?"))
print("Voce tem", quantidade_real, "reais")
print(f"Você tem {quantidade_real} reais")
print(f"Você tem {(quantidade_real):.2f} reais")
### o que é :?, o que é .2 (duas casas decimais), o que é f (significa que é float)

